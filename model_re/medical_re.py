          # 2. np.where(subject_labels_pred[0, :, 0] > 0.4)[0] → 起始位置
          #
          # 只看起始通道，逐个扫描 > 0.4 的：
          #
          # ┌──────┬──────┬───────────┐
          # │ 索引 │ 概率 │  > 0.4?   │
          # ├──────┼──────┼───────────┤
          # │ 1    │ 0.95 │ ✅  → 候选 │
          # ├──────┼──────┼───────────┤
          # │ 6    │ 0.89 │ ✅  → 候选 │
          # └──────┴──────┴───────────┘
          #
          # start = [1, 6]
          #
          # 3. np.where(subject_labels_pred[0, :, 1] > 0.4)[0] → 结束位置
          #
          # 看结束通道：
          #
          # ┌──────┬──────┬───────────┐
          # │ 索引 │ 概率 │  > 0.4?   │
          # ├──────┼──────┼───────────┤
          # │ 4    │ 0.88 │ ✅  → 候选 │
          # ├──────┼──────┼───────────┤
