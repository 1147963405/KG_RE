          #
          # 4. np.where(...)[0]
          #
          # np.where 返回一个元组 (array_of_indices,)，[0] 把它展开成一维数组。
          #
          # 最终配对
          #
          # for i in start:       # 每个起始位置
          #     j = end[end >= i] # 找它后面的第一个结束位置
          #     ...
