          # 先说结论：这两行代码把模型预测的连续概率值转成离散的实体位置索引。
          #
          # 逐步拆解：
          #
          # 1. subject_labels_pred 的形状
          #
          # (1, max_seq_len=256, 2)
          #  ↑         ↑            └─ 通道 0: 当前 token 是"主体起始"的概率
          #  batch     │               通道 1: 当前 token 是"主体结束"的概率
          #          所有 token 位置（含 padding）
          #
          # 2. subject_labels_pred[0, :, 0]
          #
          # 取第 0 个样本的"起始概率"一维数组，长度 256，每个值在 0~1 之间。
          #
          # 3. > 0.4
          #
          # 阈值过滤 — 概率超过 0.4 才算数。比如输出可能是：
          #
          # 位置:  0   1   2   3   4   5   6   7  ...  ← [CLS] 阿 莫 西  林  治  疗  高  ...
