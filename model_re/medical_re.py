#   4. 加载 checkpoint — torch.load(config.PATH_MODEL) 加载预训练好的模型权重（这就是报错的那行）
#   5. 初始化模型 — 创建 Model4s（主体抽取）和 Model4po（客体+谓词抽取），从 checkpoint 恢复权重
#   6. 配置优化器 — 把两个模型的参数合并，用 AdamW 优化器，对 bias 和 LayerNorm 参数不做权重衰减（weight_decay=0.0），其余参数 weight_decay=0.01；并从 checkpoint 恢复优化器状态
#   7. 开始训练 — 调用 train() 函数，返回新的 checkpoint
#
#   本质：这是微调 (fine-tune) 流程，在已有预训练模型基础上继续训练，而不是从零开始训练。

def run_train():
    load_schema(config.PATH_SCHEMA)
    train_path = config.PATH_TRAIN
    all_data = load_data(train_path) #加载train_example.json数据
    random.shuffle(all_data) #用途是：打乱数据集样本顺序，避免数据按固定顺序训练导致模型偏向、训练效果差

    # 8:2划分训练集、验证集
    # 1. 计算分割下标：总样本数 × 0.8，转整数
    idx = int(len(all_data) * 0.8)
    # 2. 切片取前80%作为训练集（下标 0 ~ idx-1）
    train_data = all_data[:idx]
    # 3. 切片取后20%作为验证集（下标 idx ~ 末尾）
    valid_data = all_data[idx:]

    # train
    train_data_loader = IterableDataset(train_data, True)
    num_train_data = len(train_data)
    #1.torch.load(config.PATH_MODEL) — 从磁盘加载之前训练好的checkpoint文件（model_re.pkl）
    #2.map_location = torch.device('cpu') — 强制将所有张量加载到CPU上，而不是试图加载到保存时的设备（CUDA）
    checkpoint = torch.load(config.PATH_MODEL, map_location=torch.device('cpu'))

   #1. 初始化模型 + 加载预训练权重
    model4s = Model4s()
    model4s.load_state_dict(checkpoint['model4s_state_dict']) #创建主体抽取模型，从 checkpoint 恢复之前训练好的权重。
    #需要GPU环境
    # model4s.cuda()

    model4po = Model4po()
    model4po.load_state_dict(checkpoint['model4po_state_dict']) #创建客体+谓词抽取模型，同样恢复权重。
    # model4po.cuda()

    # 2. 合并两个模型的参数
    param_optimizer = list(model4s.named_parameters()) + list(model4po.named_parameters()) #把 Model4s 和 Model4po 的所有参数合并到一个列表（含 BERT 的 1.1 亿参数 + 两个线性层），供优化器统一更新

   #3. 分组设置权重衰减
    no_decay = ['bias', 'LayerNorm.bias', 'LayerNorm.weight']
    optimizer_grouped_parameters = [
        {'params': [p for n, p in param_optimizer if not any(nd in n for nd in no_decay)], 'weight_decay': 0.01},  #不是 bias/LayerNorm,BERT 的权重矩阵、Linear 层的权重
        {'params': [p for n, p in param_optimizer if any(nd in n for nd in no_decay)], 'weight_decay': 0.0} #是 bias/LayerNorm,偏置项、LayerNorm 参数
    ]

    #4. 创建优化器 + 恢复状态
    #使用 AdamW 优化器，学习率 1e-5（BERT 微调典型值），并从 checkpoint 恢复优化器的动量/梯度历史状态，确保继续训练时学习率调度连续
    lr = config.learning_rate
    optimizer = AdamW(optimizer_grouped_parameters, lr=lr)
    optimizer.load_state_dict(checkpoint['optimizer_state_dict'])

     #5. 训练入口
    checkpoint = train(train_data_loader, model4s, model4po, optimizer)

    del train_data
    gc.collect()
    # save
    model_path = config.PATH_SAVE
    torch.save(checkpoint, model_path)
    print('saved!')

    # valid
    model4s.eval()
    model4po.eval()
    f1, precision, recall = evaluate(valid_data, True, model4s, model4po)
    print('f1: %.5f, precision: %.5f, recall: %.5f' % (f1, precision, recall))


