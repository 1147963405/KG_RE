def get_triples(content, model4s, model4po):
    if len(content) == 0:
        return []
  #  改前：
  # text_list = content.split('。')[:-1] # 按句号分句
  #
  # 改后：
  # text_list = [t for t in content.split('。') if t] # 按句号分句，过滤空串
  #
  # 为什么改： 原来的 [:-1] 是为了去掉句号结尾产生的空串（比如"a。b。" split 后是 ["a", "b", ""]，去掉最后一个）。但文本没有句号时（比如 "高血压病人不可食用阿莫西林等药物"），split 结果是 ["高血压病人不可食用阿莫西林等药物"]，[:-1] 直接把它删光了，所以 RE 返回空。
  #
  # 改后的 [t for t in ... if t] 只过滤空串，保留所有有内容的分句，不管有没有句号。

    text_list = [t for t in content.split('。') if t] # 按句号分句，过滤空串
    res = []
    for text in text_list:
        if len(text) > 128:
            text = text[:128]  # 每句限长 128 字
        triples = extract_spoes(text, model4s, model4po) # 逐句抽取
        res.append({
            'text': text,
            'triples': triples
        })
    return res

