          # end = [4, 5]
          #
          # 4. 配对（组装实体）
          #
          # for i in start:        # i = 1 → 模型说"位置 1 可能是一个实体的开头"
          #     j = end[end >= i]  # 找结束数组中 ≥1 的: [4, 5] → 第一个是 4
          #     subjects.append((1, 4))  # 得到实体 span (1, 4)
          #
          # for i in start:        # i = 6 → "位置 6 可能也是一个实体的开头"
          #     j = end[end >= i]  # 找结束数组中 ≥6 的: [] → 没有，这个起始被丢弃
          #
          # 最终 subjects = [(1, 4)]，反向映射到原文 token 索引 1~4 → "阿莫西林"。
          #
          # 可视化流程
          #
          # 位置:  [CLS]  阿    莫    西    林    治疗   高    血压  [SEP]
          # 起始:  0.01  0.95  0.03  0.02  0.10  0.02  0.89  0.01  0.01
          #                ↑                           ↑
          #               > 0.4                      > 0.4
          #                │                           │
          # 结束:  0.01  0.02  0.04  0.003  0.88  0.91  0.01  0.02  0.01
          #                                 ↑      ↑
          #                               > 0.4   > 0.4
          #                                 │      │
          #                ┌────────────────┘      └─────────┐
          #                ↓                                 ↓
          #            (1,4) → "阿莫西林"                 (6,5) 无效，丢弃
          #
          # 总结：这两行代码就是"捡起模型说哪里是开头、哪里是结尾，然后配对组装成实体"，中间用的 0.4 就是个过滤噪声的门槛。

        start = np.where(subject_labels_pred[0, :, 0] > 0.4)[0]
        end = np.where(subject_labels_pred[0, :, 1] > 0.4)[0]

        subjects = []
        for i in start:
            j = end[end >= i]
            if len(j) > 0:
                j = j[0]
                subjects.append((i, j))

        if len(subjects) == 0:
            return []

          #   这行代码把 subjects 列表转换成 PyTorch 张量，然后拍平保留 batch 维度。一步步拆：
          #
          # 1. subjects 的内容
          #
          # subjects = [(1, 4), (8, 12)]   # 两个主体，每个是 (start, end)
          #
          # 2. torch.tensor(subjects)
          #
          # # Python list of tuples → PyTorch 张量
          # tensor([[1, 4],
          #         [8, 12]])    # shape (2, 2)
          #
          # 3. .view(1, -1)
          #
          # - 1 → batch 维度为 1
          # - -1 → 剩余维度自动计算（原有 4 个元素，所以是 4）
          #
          # tensor([[1, 4, 8, 12]])    # shape (1, 4)
          #
          # 把 (2, 2) 拉直成 (1, 4)。
          #
          # ---
          # 为什么这样处理？ 看它怎么被用的：
          #
          # # extract_spoes 第 972 行
          # for s in subjects:
          #     object_labels_pred = model4po(hidden_states, subject_ids, mask_ids)
          #
          # 而在 Model4po.forward()（第 239-241 行）中：
          #
          # for b in range(hidden_states.shape[0]):        # hidden_states batch=1
          #     s_start = batch_subject_ids[b][0]           # 取 subject_ids[0][0] → start
          #     s_end = batch_subject_ids[b][1]             # 取 subject_ids[0][1] → end
          #     s = hidden_states[b][s_start] + hidden_states[b][s_end]  # 取该 span 的起止向量并求和
          #
          # 所以 view(1, -1) 后的形状 (1, 4) 意味着：
          # - batch_subject_ids[0] = [1, 4, 8, 12]
          # - batch_subject_ids[0][0] = 1, batch_subject_ids[0][1] = 4
          #
          # 实际上只用了每个 subject 的起始/结束 index，但 .view(1,-1) 把所有 subject 的坐标"首尾相接"进了同一个 batch。同一句里多个 subject 时，因为这个循环只迭代 batch_size=1，所以只用到了第一个 subject 的坐标。
          #
          # 本质是为了匹配 Model4po 的 batch 接口：它期望输入形状 (batch, ...)，而一个句子的所有 subject 位置拼接成一行。
        spoes = []
          # 改前：循环外拼装所有主体，Model4po 只用第一组
          # subject_ids = torch.tensor(subjects).view(1, -1)
          # for s in subjects:
          #     object_labels_pred = model4po(hidden_states, subject_ids, mask_ids)
          #
          # # 改后：每轮循环只用当前主体的坐标
